         <h5>Retail and commercial banks</h5>
         <p>Some commercial banks have specialised trade finance divisions, which offer facilities to businesses. Commercial banks represent the majority share of financial institutions globally, although they range in size from small and niche banks to large multinational banks.	</p> 
    <p>The banking services offered by trade finance commercial banks include: issuing letters of credit, accepting drafts and negotiating notes, bills of exchange and documentary collections. The advantage of larger commercial banks over smaller niche banks is twofold: their global presence (they may have foreign subsidiaries which makes L/C confirmation cost effective), and their credibility.
</p>
       
       <h5>Alternative Finance and Non-Bank Funders</h5>
   <p>There are many types of financial institutions that do not use public deposits as a funding resource. Funding sources include crowd-funded (pooled) investment, private investment and public market sourced capital.
</p>

    <p>Traditional ‘receivables-backed finance’ has been disrupted by smaller finance platforms since the economic crisis. This has been driven by a decrease in appetite for risk by larger banks, which has opened the doors to agile smaller finance lenders, who can fill the gap. Private investment funds and larger banks provide capital to alternative trade financiers. Crowd-lending (peer to peer) finance has also entered the trade finance sector. In addition to this, new technologies to disrupt the somewhat lengthy application process for certain types of trade finance make it easier to assess risk, supply credit and documentation to importers and exporters.
</p>
     
