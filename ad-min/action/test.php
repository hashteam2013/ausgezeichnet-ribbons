      <h5>Why is trade financing important?</h5>
      <p> Trade financing (also known as supply chain and export finance) is a huge driver of economic development and helps maintain the flow of credit in supply chains. It is predicted that 80-90% of global trade is reliant on trade and supply chain finance, and is estimated to be worth around USD $10 trillion a year.</p>
    
      <h5>What does trade finance include?</h5>
      <p> Trade finance includes the following: </p>
      
      <ul class="faq-listing">
        <li> Lending facilities</li>
        <li> Issuing Letters of Credit (LCs)</li>
        <li> Export factoring (companies receive funds against invoices or accounts receivable)</li>
        <li> Forfaiting (purchasing the receivables or traded goods from an exporter)</li>
        <li> Export credits (to reduce risks to funders when providing trade or supply chain finance)</li>
        <li> Insurance (during delivery and shipping, also covers currency risk and exposure)</li>
      </ul>
      
       <h5>What are the methods of payment for trade finance?</h5>
       <p>As the least risky product for the seller, a cash advance requires payment to the exporter or seller before the goods or services have been shipped. Cash advances are very common with lower value orders, and helps provide exporters / sellers with up front cash to ship the goods, and no risk of late or no payment.</p>
      
        <h5>Letters of Credit (LCs)</h5>
        <p>Letters of credit (LCs), also known as documentary credits are financial, legally binding instruments, issued by banks or specialist trade finance institutions, which pay the exporter on behalf of the buyer, if the terms specified in the LC are fulfilled.</p>
        <p>An LC requires an importer and an exporter, with an issuing bank and a confirming (or advising) bank respectively. The financiers and their creditworthiness are crucial for this type of trade finance: it is called credit enhancement – the issuing and confirming bank replace the guarantee of payment from the importer and exporter. In this section, and in most cases, we may consider the importer as the buyer and the exporter as the seller.</p>
       
         <h5>Documentary collection</h5>
         <p>Documentary collections differ from a Letter of Credit (read our blog post on the differences between DCs and LCs).</p> 
         <p>In the case of DC, the exporter will request payment by presenting its shipping and collection documents to their remitting bank. The remitting bank then forwards these documents on to the bank of the importer. The importers bank will then pay the exporters bank, which will credit those funds to the exporter.</p>
         
         <h5>Open Accounts</h5>
         <p>An open account is a transaction where the importer pays the exporter 30 – 90 days after the goods have arrived from the exporter. This is obviously advantageous to the importer and carries substantial risk for the exporter – it often occurs if the relationship and trust between the two parties is strong.</p> 
         <p>Open accounts help increase competitiveness in export markets, and buyers often push for exporters and sellers to trade on open account terms. As a result, exporters may seek export finance to fund working capital whilst waiting for the payment.</p>
         
         
